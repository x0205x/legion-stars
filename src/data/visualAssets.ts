export function getVisual(id: string): VisualAsset {
  const v = VISUAL_ASSETS.find((x) => x.id === id);
  if (!v) throw new Error(`Unknown visual: ${id}`);
  return v;
}

export function visualsForFaction(factionId: string): VisualAsset[] {
  return VISUAL_ASSETS.filter((v) => v.factionId === factionId);
}

export function visualsByCategory(category: VisualAsset["category"]): VisualAsset[] {
  return VISUAL_ASSETS.filter((v) => v.category === category);
}
